#!/bin/bash

source ./../env.sh

cd "$(dirname "$0")"

NEXUS_RELEASE=3.25.1
NEXUS_VERSION=3.25.1-04
NEXUS_DOWNLOAD_SHA256_HASH=0bfd23958a1329bb3ed36cbe6fe15efd85f9ab2ed7a3235576c2eccc9173cf2e

BUILD_ARG="${BUILD_ARG} --build-arg NEXUS_RELEASE=${NEXUS_RELEASE} --build-arg NEXUS_VERSION=${NEXUS_VERSION} --build-arg NEXUS_DOWNLOAD_SHA256_HASH=${NEXUS_DOWNLOAD_SHA256_HASH}"

docker build ${BUILD_ARG} -t ${REGISTRY}/nexus:${NEXUS_VERSION} .
